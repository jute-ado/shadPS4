#include <array>

#include <gtest/gtest.h>

#include "imgui/renderer/png_decode.h"

TEST(PngDecode, RejectsEmptyInput) {
    EXPECT_FALSE(ImGui::Core::TextureManager::DecodePngRgba({}));
}

TEST(PngDecode, RejectsTruncatedInput) {
    constexpr std::array<u8, 8> signature = {0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A};

    EXPECT_FALSE(ImGui::Core::TextureManager::DecodePngRgba(signature));
}

TEST(PngDecode, DecodesValidInputToRgba) {
    constexpr std::array<u8, 68> png = {
        0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D, 0x49, 0x48,
        0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x08, 0x04, 0x00, 0x00,
        0x00, 0xB5, 0x1C, 0x0C, 0x02, 0x00, 0x00, 0x00, 0x0B, 0x49, 0x44, 0x41, 0x54, 0x78,
        0xDA, 0x63, 0x64, 0xF8, 0x0F, 0x00, 0x01, 0x05, 0x01, 0x01, 0x27, 0x18, 0xE3, 0x66,
        0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82,
    };

    const auto decoded = ImGui::Core::TextureManager::DecodePngRgba(png);

    ASSERT_TRUE(decoded.has_value());
    EXPECT_EQ(decoded->width, 1u);
    EXPECT_EQ(decoded->height, 1u);
    EXPECT_EQ(decoded->SizeBytes(), 4u);
    EXPECT_NE(decoded->pixels, nullptr);
}
